    /// <summary>
    /// Определяет регион по ИНН (первые две цифры)
    /// </summary>
    /// <param name="inn">ИНН должника</param>
    /// <returns>Кортеж (код региона, название региона) или null, если не удалось определить</returns>
    public static (string RegionCode, string RegionName)? GetRegionByInn(string? inn)
    {
        if (string.IsNullOrWhiteSpace(inn) || inn.Length < 2)
        {
            return null;
        }

        var regionCode = inn.Substring(0, 2);
        return GetRegionByCode(regionCode);
    }

    /// <summary>
    /// Получает название региона по коду региона
    /// </summary>
    /// <param name="regionCode">Код региона (две цифры)</param>
    /// <returns>Кортеж (код региона, название региона) или null, если не удалось определить</returns>
    public static (string RegionCode, string RegionName)? GetRegionByCode(string? regionCode)
    {
        if (string.IsNullOrWhiteSpace(regionCode) || regionCode.Length != 2)
        {
            return null;
        }

        if (RegionCodes.TryGetValue(regionCode, out var regionName))
        {
            return (regionCode, regionName);
        }

        return null;
    }
